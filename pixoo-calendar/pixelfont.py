CHAR_W = 5  # 4px glyph + 1px spacing
CHAR_H = 7  # 6px glyph + 1px spacing


def draw_text(img: Image.Image, x: int, y: int, text: str, color: tuple, max_w: int = 64):
    """Draw pixel-perfect text onto an image. No antialiasing."""
    for ch in text:
        if x + 4 > max_w:
            break
        glyph = GLYPHS.get(ch, GLYPHS.get(ch.upper(), GLYPHS[" "]))
        for row_idx, row in enumerate(glyph):
            for col in range(4):
                if row & (0b1000 >> col):
                    px = x + col
                    py = y + row_idx
                    if 0 <= px < img.width and 0 <= py < img.height:
                        img.putpixel((px, py), color)
        x += CHAR_W


def text_width(text: str) -> int:
    """Get the pixel width of a string."""
    return len(text) * CHAR_W


