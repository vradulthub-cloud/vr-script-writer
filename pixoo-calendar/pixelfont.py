    """Draw large digits for countdown display."""
    for ch in text:
        glyph = BIG_DIGITS.get(ch)
        if glyph is None:
            x += BIG_W
            continue
        for row_idx, row in enumerate(glyph):
            for col in range(6):
                if row & (0b100000 >> col):
                    px = x + col
                    py = y + row_idx
                    if 0 <= px < img.width and 0 <= py < img.height:
                        img.putpixel((px, py), color)
        x += BIG_W


