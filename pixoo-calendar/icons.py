def draw_icon(img: Image.Image, x: int, y: int, icon_name: str):
    """Draw a 12x12 pixel art icon onto an image."""
    icon = ICONS.get(icon_name, ICONS["cloud"])
    for row_idx, row in enumerate(icon):
        for col_idx, pixel in enumerate(row):
            if pixel is not None:
                px = x + col_idx
                py = y + row_idx
                if 0 <= px < img.width and 0 <= py < img.height:
                    img.putpixel((px, py), pixel)
